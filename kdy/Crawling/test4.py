from konlpy.tag import Okt
from gensim import corpora
from gensim.models import LdaModel

def lda_keyword_extraction(text, num_keywords):
    # Konlpy의 Okt 형태소 분석기를 사용하여 문장을 토큰화합니다.
    okt = Okt()
    tokens = okt.nouns(text)

    # 토큰을 사용하여 사전과 코퍼스를 생성합니다.
    dictionary = corpora.Dictionary([tokens])
    corpus = [dictionary.doc2bow(text) for text in [tokens]]

    # LDA 모델을 학습합니다.
    lda_model = LdaModel(corpus, num_topics=1, id2word=dictionary, passes=10)

    # 가장 관련성이 높은 키워드를 추출합니다.
    keywords = lda_model.show_topic(0, num_keywords)

    # 키워드와 각 키워드의 가중치를 반환합니다.
    return keywords

